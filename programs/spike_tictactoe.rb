In tic tac toe, there are two players. one assumes the symbol 'x' and the other, the symbol 'o'. One player goes first choosing a square on the board to mark with their symbol. Then the next player goes. In order to win, one player must have marked three squares in a line, either vertically, horizontally, or diagonally.We need to validate a win by referencing the state of the board and seeing if any of the above cases are true. if so, there is a winner. Return the winner

